#kerj be ket szamot, az alapmőveleteket vegezd el veluk es irasd ki az eredmenyt

a = input ('Add meg az első számot:')
b = input ('Add meg a második számot:')
szam1 = int(a)
szam2 = int(b)
osszeg = szam1 + szam2
kulonbseg = szam1 - szam2
hanyados = szam1 / szam2
szorzat = szam1 * szam2
maradek = szam1 % szam2
power = szam1 ** szam2
print ('ez a ket szam összege:', osszeg)
print ('ez a ket szam kulonbsege:', kulonbseg)
print ('ez a ket szam hanyadosa:', hanyados)
print ('ez a ket szam szorzat:', szorzat)
print ('ez a ket szam hanyadosanak a maradeka:', maradek)
print ('ez a ket szam hatványa:', power)